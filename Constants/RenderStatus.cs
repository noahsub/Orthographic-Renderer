namespace Orthographic.Renderer.Constants;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// RENDER STATUS ENUM
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/// <summary>
/// The status values for a render operation.
/// </summary>
public enum RenderStatus
{
    /// <summary>
    /// The render is pending.
    /// </summary>
    Pending,

    /// <summary>
    /// The render is in progress.
    /// </summary>
    InProgress,

    /// <summary>
    /// The render is completed.
    /// </summary>
    Completed,

    /// <summary>
    /// The render has failed.
    /// </summary>
    Failed,
}