namespace Orthographic.Renderer.Entities;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// POSITION CLASS
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/// <summary>
/// Represents a position in 3D space with rotation.
/// </summary>
public class Position
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // PROPERTIES
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /// <summary>
    /// The X coordinate.
    /// </summary>
    public float X { get; }

    /// <summary>
    /// The Y coordinate.
    /// </summary>
    public float Y { get; }

    /// <summary>
    /// The Z coordinate.
    /// </summary>
    public float Z { get; }

    /// <summary>
    /// The rotation around the X axis.
    /// </summary>
    public float Rx { get; }

    /// <summary>
    /// The rotation around the Y axis.
    /// </summary>
    public float Ry { get; }

    /// <summary>
    /// The rotation around the Z axis.
    /// </summary>
    public float Rz { get; }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // INITIALIZATION
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /// <summary>
    /// Initializes a new instance of the <see cref="Position"/> class.
    /// </summary>
    /// <param name="x">The X coordinate.</param>
    /// <param name="y">The Y coordinate.</param>
    /// <param name="z">The Z coordinate.</param>
    /// <param name="rx">The rotation around the X axis.</param>
    /// <param name="ry">The rotation around the Y axis.</param>
    /// <param name="rz">The rotation around the Z axis.</param>
    public Position(float x, float y, float z, float rx, float ry, float rz)
    {
        X = x;
        Y = y;
        Z = z;
        Rx = rx;
        Ry = ry;
        Rz = rz;
    }
}
