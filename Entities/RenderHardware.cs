namespace Orthographic.Renderer.Entities;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// RENDER HARDWARE CLASS
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/// <summary>
/// Represents hardware capable of rendering such as a GPU or CPU.
/// </summary>
public class RenderHardware
{
    /// <summary>
    /// The name of the hardware component.
    /// </summary>
    public string Name { get; set; }
    
    /// <summary>
    /// The type of the hardware component.
    /// </summary>
    public string Type { get; set; }
    
    /// <summary>
    /// The frameworks that the hardware supports.
    /// </summary>
    public List<string> Frameworks { get; set; }

    /// <summary>
    /// Creates a new instance of the <see cref="RenderHardware"/> class.
    /// </summary>
    /// <param name="name">The name of the hardware component.</param>
    /// <param name="frameworks">The frameworks that the hardware supports.</param>
    public RenderHardware(string name, List<string> frameworks)
    {
        // Set the name
        Name = name;
        
        // Check if the hardware is a GPU or CPU and set the type accordingly
        if (!frameworks.Contains("CPU CYCLES"))
        {
            Type = "GPU";
        }
        
        else
        {
            Type = "CPU";
        }
        
        // Set the frameworks
        Frameworks = frameworks;
    }
}