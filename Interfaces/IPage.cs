namespace Orthographic.Renderer.Interfaces;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// IPAGE INTERFACE
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
public interface IPage
{
    /// <summary>
    /// Called when the page is first created. Code that needs to be loaded during the splash screen should be
    /// placed here.
    /// </summary>
    void Initialize();

    /// <summary>
    /// Called when the user first navigates to the page.
    /// </summary>
    void OnFirstLoad();

    /// <summary>
    /// Called whenever the user navigates to the page.
    /// </summary>
    void OnNavigatedTo();
}
